const solution = require('./solution');

describe('비밀지도', () => {
  it('올바른 값을 리턴한다', () => {
    expect(solution(5, [9, 20, 28, 18, 11], [30, 1, 21, 17, 28])).toStrictEqual(["#####", "# # #", "### #", "#  ##", "#####"]);
  });
  it('올바른 값을 리턴한다', () => {
    expect(solution(	6, [46, 33, 33, 22, 31, 50], [27, 56, 19, 14, 14, 10])).toStrictEqual(["######", "###  #", "##  ##", " #### ", " #####", "### # "]);
  });
});