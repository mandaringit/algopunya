import Foundation

// Character는 말 그대로 "문자"를 의미
let alphabetA:Character = "A"
print(alphabetA)

// Character 값에 유니코드 문자를 사용할 수 있다.
let commandCharacter:Character = "💘"
print(commandCharacter)

let 한글이름변수:Character = "ㄱ"

// 한글도 유니코드 문자에 속하므로 스위프트 코드 변수 이름으로 사용 가능.
print("한글의 첫 자음: \(한글이름변수)")

